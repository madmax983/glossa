//! The Diplomat (ὁ Πρέσβυς) - C Header Generator
//!
//! This module implements the "Diplomat" tool, which inspects the definitions
//! within a ΓΛΩΣΣΑ program and translates them into a C/C++ `.h` header file.
//!
//! # Purpose
//!
//! Just as a diplomat negotiates between foreign powers, this tool enables
//! FFI (Foreign Function Interface) interoperability. It automatically generates
//! C structs and function prototypes from Glossa's `εἶδος` and `ἔργον` definitions,
//! allowing Glossa code to interface seamlessly with C/C++ libraries.

use crate::semantic::{AnalyzedStatement, GlossaType};
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Translates a `GlossaType` into its corresponding C/C++ type equivalent.
fn glossa_type_to_c(g_type: &GlossaType) -> String {
    match g_type {
        GlossaType::Number => "int64_t".to_string(),
        GlossaType::String => "char*".to_string(),
        GlossaType::Boolean => "bool".to_string(),
        GlossaType::Unit => "void".to_string(),
        GlossaType::Struct { name, .. } => name.to_string(),
        // Fallback for pointers to unknown/complex structures
        _ => "void*".to_string(),
    }
}

/// Runs the Diplomat tool to generate C headers from Glossa types and functions.
///
/// The Diplomat (Πρέσβυς) tool reads the provided source file, compiles it, and automatically
/// generates a corresponding `.h` C header file containing struct definitions and
/// function prototypes.
///
/// # Examples
///
/// ```rust,no_run
/// use glossa::tools::diplomat::run_diplomat;
/// use std::path::Path;
///
/// let input = Path::new("ffi.γλ");
/// if let Err(e) = run_diplomat(&input) {
///     eprintln!("Header generation failed: {}", e);
/// }
/// ```
pub fn run_diplomat(input: &Path) -> Result<()> {
    if !input.exists() {
        return Err(miette::miette!("Ἀρχεῖον οὐχ εὑρέθη: {}", input.display()));
    }

    let status = Status::start_with_symbol("Πρέσβυς (Generating C Header)", "🤝");

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            status.error("Σφάλμα ἀρχείου (File Error)");
            return Err(e);
        }
    };

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            return Err(e);
        }
    };

    status.success();

    let mut output = String::new();
    let file_stem = input.file_stem().unwrap_or_default().to_string_lossy();
    let header_guard = format!("{}_H", file_stem.to_uppercase());

    output.push_str("/* Auto-generated by ΓΛΩΣΣΑ Diplomat */\n\n");
    let _ = writeln!(output, "#ifndef {}", header_guard);
    let _ = writeln!(output, "#define {}\n", header_guard);
    output.push_str("#include <stdint.h>\n");
    output.push_str("#include <stdbool.h>\n\n");
    output.push_str("#ifdef __cplusplus\n");
    output.push_str("extern \"C\" {\n");
    output.push_str("#endif\n\n");

    // Pass 1: Generate Structs
    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            let _ = writeln!(output, "typedef struct {{");
            for (field_name, field_type) in fields {
                let c_type = glossa_type_to_c(field_type);
                let _ = writeln!(output, "    {} {};", c_type, field_name);
            }
            let _ = writeln!(output, "}} {};\n", name);
        }
    }

    // Pass 2: Generate Function Prototypes
    for stmt in &program.statements {
        if let AnalyzedStatement::FunctionDef {
            name,
            params,
            return_type,
            ..
        } = stmt
        {
            let ret_c_type = return_type
                .as_ref()
                .map(glossa_type_to_c)
                .unwrap_or_else(|| "void".to_string());

            let mut c_params = Vec::new();
            for (p_name, p_type) in params {
                let p_c_type = p_type
                    .as_ref()
                    .map(glossa_type_to_c)
                    .unwrap_or_else(|| "void*".to_string());
                c_params.push(format!("{} {}", p_c_type, p_name));
            }
            let param_list = if c_params.is_empty() {
                "void".to_string()
            } else {
                c_params.join(", ")
            };

            let _ = writeln!(output, "{} {}({});\n", ret_c_type, name, param_list);
        }
    }

    output.push_str("#ifdef __cplusplus\n");
    output.push_str("}\n");
    output.push_str("#endif\n\n");
    let _ = writeln!(output, "#endif // {}", header_guard);

    let output_path = input.with_extension("h");
    if let Err(e) = std::fs::write(&output_path, output) {
        return Err(miette::miette!("Failed to write header file: {}", e));
    }

    println!();
    println!("   {}", "Γ Λ Ω Σ Σ Α   D I P L O M A T".bold().cyan());
    println!("   {}", "C Header Generated".italic().dim());
    println!();
    println!(
        "   {} {}",
        "Saved to:".bold(),
        output_path.display().to_string().cyan()
    );
    println!();

    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_glossa_type_to_c() {
        assert_eq!(glossa_type_to_c(&GlossaType::Number), "int64_t");
        assert_eq!(glossa_type_to_c(&GlossaType::String), "char*");
        assert_eq!(glossa_type_to_c(&GlossaType::Boolean), "bool");
        assert_eq!(glossa_type_to_c(&GlossaType::Unit), "void");
        assert_eq!(glossa_type_to_c(&GlossaType::Unknown), "void*");
    }

    #[test]
    fn test_run_diplomat() {
        let dir = tempfile::tempdir().unwrap();
        let input_path = dir.path().join("ffi.γλ");

        let source = "
        εἶδος Χρήστης ὁρίζειν {
            ὄνομα ὀνόματος.
            ἡλικία ἀριθμοῦ.
        }.

        προσθεσις ὁρίζειν τῷ ξ ἀριθμοῦ τῷ ψ ἀριθμοῦ· δός ξ ψ ἄθροισμα.
        ";
        std::fs::write(&input_path, source).unwrap();

        let result = run_diplomat(&input_path);
        assert!(result.is_ok());

        let output_path = input_path.with_extension("h");
        assert!(output_path.exists());

        let h_content = std::fs::read_to_string(&output_path).unwrap();
        assert!(h_content.contains("typedef struct {"));
        assert!(h_content.contains("char* ὄνομα;"));
        assert!(h_content.contains("int64_t ἡλικία;"));
        assert!(h_content.contains("} Χρήστης;"));
        assert!(h_content.contains("int64_t προσθεσις(int64_t ξ, int64_t ψ);"));
    }
}
