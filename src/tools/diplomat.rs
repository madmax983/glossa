//! The Diplomat (ὁ Διπλωμάτης) - C Header Generator
//!
//! Generates C headers (.h) from Glossa types for FFI compatibility.

use crate::semantic::{AnalyzedStatement, GlossaType};
use crate::tools::runner::{analyze_source, load_source};
use crate::tools::ui::Status;
use comfy_table::{Attribute, Cell, Color, Table, presets};
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Runs the Diplomat tool to generate C headers from Glossa types.
pub fn run_diplomat(input: &Path) -> Result<()> {
    let status = Status::start_with_symbol("Διπλωμάτης (Generating C Header)", "🤝");

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            status.error("Σφάλμα ἀρχείου (File Error)");
            return Err(e);
        }
    };

    let program = match analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            return Err(e);
        }
    };

    status.success();

    let mut output = String::new();
    output.push_str("/* Auto-generated by ΓΛΩΣΣΑ Diplomat */\n");
    output.push_str("#ifndef GLOSSA_GENERATED_H\n");
    output.push_str("#define GLOSSA_GENERATED_H\n\n");
    output.push_str("#include <stdint.h>\n");
    output.push_str("#include <stdbool.h>\n\n");

    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            let _ = writeln!(output, "typedef struct {} {{", name);
            for (field_name, field_type) in fields {
                let c_type = glossa_type_to_c(field_type);
                let _ = writeln!(output, "    {} {};", c_type, field_name);
            }
            let _ = writeln!(output, "}} {};\n", name);
        }
    }

    output.push_str("#endif /* GLOSSA_GENERATED_H */\n");

    println!();
    println!("   {}", "Γ Λ Ω Σ Σ Α   D I P L O M A T".bold().cyan());
    println!("   {}", "C Header (FFI)".italic().dim());
    println!();

    let mut table = Table::new();
    table.load_preset(presets::UTF8_FULL);

    table.set_header(vec![
        Cell::new("C Header")
            .add_attribute(Attribute::Bold)
            .fg(Color::Cyan),
    ]);

    let formatted_code = format!("```c\n{}\n```", output.trim());
    table.add_row(vec![Cell::new(formatted_code)]);

    println!("{table}");
    println!();

    Ok(())
}

fn glossa_type_to_c(g_type: &GlossaType) -> String {
    match g_type {
        GlossaType::Number => "int64_t".to_string(),
        GlossaType::String => "char*".to_string(),
        GlossaType::Boolean => "bool".to_string(),
        _ => "void*".to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_glossa_type_to_c() {
        assert_eq!(glossa_type_to_c(&GlossaType::Number), "int64_t");
        assert_eq!(glossa_type_to_c(&GlossaType::String), "char*");
        assert_eq!(glossa_type_to_c(&GlossaType::Boolean), "bool");
        assert_eq!(glossa_type_to_c(&GlossaType::Unknown), "void*");
    }
}
