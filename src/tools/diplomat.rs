//! The Diplomat (ὁ Διπλωμάτης) - C Header Generator
//!
//! This module implements the "Diplomat" tool, which inspects the type definitions
//! (`εἶδος`) within a ΓΛΩΣΣΑ program and translates them into C header (`.h`) structures.
//!
//! # Purpose
//!
//! The Diplomat allows ΓΛΩΣΣΑ programs to achieve FFI interoperability, letting them
//! interface with standard C libraries. Because Ancient Greek should be ABI-compatible
//! with C.

use crate::semantic::{AnalyzedProgram, AnalyzedStatement, GlossaType};
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Convert a Glossa type to a C type string
fn glossa_type_to_c(glossa_type: &GlossaType) -> &'static str {
    match glossa_type {
        GlossaType::Number => "int64_t",
        GlossaType::String => "char*",
        GlossaType::Boolean => "bool",
        _ => "void*", // Generic fallback for unsupported types
    }
}

/// Runs the Diplomat tool to generate C headers from Glossa types.
pub fn run_diplomat(input: &Path) -> Result<()> {
    if !input.exists() {
        return Err(miette::miette!("Ἀρχεῖον οὐχ εὑρέθη: {}", input.display()));
    }

    let status = Status::start_with_symbol("Διπλωμάτης (Generating C Headers)", "📜");

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            status.error("Σφάλμα ἀρχείου (File Error)");
            return Err(e);
        }
    };

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            return Err(e);
        }
    };

    status.success();

    let output = generate_c_headers(&program);

    println!("\n{}", "C Header Export:".bold().green());
    println!("{}", output);

    Ok(())
}

fn generate_c_headers(program: &AnalyzedProgram) -> String {
    let mut output = String::new();
    output.push_str("/* Auto-generated by ΓΛΩΣΣΑ Diplomat */\n");
    output.push_str("#include <stdint.h>\n");
    output.push_str("#include <stdbool.h>\n\n");

    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            let _ = writeln!(output, "typedef struct {{");
            for (field_name, field_type) in fields {
                let c_type = glossa_type_to_c(field_type);
                let _ = writeln!(output, "    {} {};", c_type, field_name);
            }
            let _ = writeln!(output, "}} {};\n", name);
        }
    }

    output
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::semantic::Scope;
    use smol_str::SmolStr;

    #[test]
    fn test_diplomat_generation() {
        let mut program = AnalyzedProgram {
            statements: Vec::new(),
            scope: Scope::new(),
        };
        program.statements.push(AnalyzedStatement::TypeDefinition {
            name: SmolStr::new("Point"),
            fields: vec![
                (SmolStr::new("x"), GlossaType::Number),
                (SmolStr::new("y"), GlossaType::Number),
            ],
        });

        let output = generate_c_headers(&program);

        assert!(output.contains("typedef struct {"));
        assert!(output.contains("    int64_t x;"));
        assert!(output.contains("    int64_t y;"));
        assert!(output.contains("} Point;"));
    }
}
