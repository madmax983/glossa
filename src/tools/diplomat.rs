//! The Diplomat (ὁ Διπλωμάτης) - TypeScript Exporter
//!
//! This module implements the "Diplomat" tool, an exporter that translates ΓΛΩΣΣΑ
//! types (`εἶδος`) and functions (`ἔργον`) into TypeScript Definition (`.d.ts`) files.
//!
//! # Purpose
//!
//! Bridges Ancient Greek backend types with modern web frontends by translating
//! Glossa concepts to TypeScript `interface` and `declare function` definitions.

use crate::semantic::GlossaType;
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Recursively translate a GlossaType to a TypeScript type string.
fn translate_to_ts(t: &GlossaType) -> String {
    match t {
        GlossaType::Number => "number".to_string(),
        GlossaType::String => "string".to_string(),
        GlossaType::Boolean => "boolean".to_string(),
        GlossaType::List(inner) => format!("Array<{}>", translate_to_ts(inner)),
        GlossaType::Set(inner) => format!("Set<{}>", translate_to_ts(inner)),
        GlossaType::Map(k, v) => format!("Map<{}, {}>", translate_to_ts(k), translate_to_ts(v)),
        GlossaType::Option(inner) => format!("{} | null", translate_to_ts(inner)),
        GlossaType::Result(ok, err) => {
            format!("{} | Error<{}>", translate_to_ts(ok), translate_to_ts(err))
        }
        GlossaType::Struct { name, .. } => name.to_string(),
        GlossaType::Function { params, returns } => {
            let mut ts_params = String::new();
            for (i, p) in params.iter().enumerate() {
                if i > 0 {
                    ts_params.push_str(", ");
                }
                ts_params.push_str(&format!("arg{}: {}", i, translate_to_ts(p)));
            }
            format!("({}) => {}", ts_params, translate_to_ts(returns))
        }
        GlossaType::Unknown => "any".to_string(),
        GlossaType::Unit => "void".to_string(),
    }
}

/// Run the Diplomat tool on a file
pub fn run_diplomat(input: &Path) -> Result<()> {
    let source = load_source(input)?;
    let status = Status::start_with_symbol("Διπλωματία (Generating TypeScript)", "📜");

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα (Error)");
            return Err(e);
        }
    };

    let mut ts = String::with_capacity(4096);
    let filename = input.file_name().unwrap_or_default().to_string_lossy();
    let _ = writeln!(ts, "// Generated by Glossa Diplomat from {}", filename);
    let _ = writeln!(ts, "// Do not edit manually.\n");

    let types: Vec<_> = program.scope.types().collect();
    for (name, type_def) in types {
        if let GlossaType::Struct { fields, .. } = type_def {
            let _ = writeln!(ts, "export interface {} {{", name);
            for (field_name, field_type) in fields {
                let _ = writeln!(ts, "  {}: {};", field_name, translate_to_ts(field_type));
            }
            let _ = writeln!(ts, "}}\n");
        }
    }

    let functions: Vec<_> = program.scope.functions().collect();
    for func in functions {
        let mut param_str = String::new();
        for (i, p) in func.param_types.iter().enumerate() {
            if i > 0 {
                param_str.push_str(", ");
            }
            param_str.push_str(&format!("arg{}: {}", i, translate_to_ts(p)));
        }
        let ret_str = func
            .return_type
            .as_ref()
            .map(translate_to_ts)
            .unwrap_or_else(|| "void".to_string());

        let _ = writeln!(
            ts,
            "export declare function {}({}): {};",
            func.name, param_str, ret_str
        );
    }

    let output_path = input.with_extension("d.ts");
    if let Err(e) = std::fs::write(&output_path, &ts) {
        status.error("Σφάλμα ἀρχείου (File Error)");
        return Err(miette::miette!("Failed to write TypeScript file: {}", e));
    }

    status.success();

    println!();
    println!("   {}", "Γ Λ Ω Σ Σ Α   D I P L O M A T".bold().cyan());
    println!("   {}", "TypeScript Definitions Generated".italic().dim());
    println!();
    println!(
        "   {} {}",
        "Saved to:".bold(),
        output_path.display().to_string().cyan()
    );
    println!();

    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;
    use tempfile::tempdir;

    #[test]
    fn test_run_diplomat_success() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api.γλ");
        fs::write(&input_path, "εἶδος Χρήστης ὁρίζειν { ὄνομα ὀνόματος. }.").unwrap();

        let result = run_diplomat(&input_path);
        assert!(result.is_ok());

        let output_path = input_path.with_extension("d.ts");
        assert!(output_path.exists());
        let ts_code = fs::read_to_string(&output_path).unwrap();
        println!("TS CODE: {}", ts_code);
        assert!(ts_code.contains("export interface"));
        // Types in the scope are stored with their names normalized to lowercase
        // with stripped accents.
        assert!(ts_code.contains("χρηστης"));
        assert!(ts_code.contains("ονομα: string"));
    }

    #[test]
    fn test_run_diplomat_mapping() {
        assert_eq!(translate_to_ts(&GlossaType::Number), "number");
        assert_eq!(translate_to_ts(&GlossaType::String), "string");
        assert_eq!(translate_to_ts(&GlossaType::Boolean), "boolean");
        assert_eq!(
            translate_to_ts(&GlossaType::List(Box::new(GlossaType::Number))),
            "Array<number>"
        );
        assert_eq!(
            translate_to_ts(&GlossaType::Option(Box::new(GlossaType::String))),
            "string | null"
        );
        assert_eq!(
            translate_to_ts(&GlossaType::Function {
                params: vec![GlossaType::Number],
                returns: Box::new(GlossaType::Boolean)
            }),
            "(arg0: number) => boolean"
        );
    }
}
