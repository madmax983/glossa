//! The Ptolemy (ὁ Πτολεμαῖος) - TypeScript Definition Generator
//!
//! This module implements the "Ptolemy" tool, which inspects the type definitions
//! (`εἶδος`) and functions (`ἔργα`) within a ΓΛΩΣΣΑ program and translates them into TypeScript
//! definition (`.d.ts`) files.
//!
//! # Purpose
//!
//! Ptolemy mapped the stars and geography of the ancient world. This tool maps the
//! semantic structure of a ΓΛΩΣΣΑ program into a modern ecosystem (TypeScript), enabling
//! seamless interoperability.

use crate::semantic::{AnalyzedStatement, GlossaType};
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use comfy_table::{Attribute, Cell, Color, Table, presets};
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Runs the Ptolemy tool to generate TypeScript definitions from Glossa types and functions.
pub fn run_ptolemy(input: &Path) -> Result<()> {
    if !input.exists() {
        return Err(miette::miette!("Ἀρχεῖον οὐχ εὑρέθη: {}", input.display()));
    }

    let status = Status::start_with_symbol("Πτολεμαῖος (Generating TypeScript Definitions)", "📜");

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            status.error("Σφάλμα ἀρχείου (File Error)");
            return Err(e);
        }
    };

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            return Err(e);
        }
    };

    status.success();

    let mut output = String::new();
    output.push_str("// Auto-generated by ΓΛΩΣΣΑ Ptolemy\n");
    output.push_str("// TypeScript Definitions\n\n");

    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            let _ = writeln!(output, "export interface {} {{", name);
            for (field_name, field_type) in fields {
                let ts_type = glossa_type_to_ts(field_type);
                let _ = writeln!(output, "    {}: {};", field_name, ts_type);
            }
            output.push_str("}\n\n");
        }
    }

    for func in program.scope.functions() {
        // Skip the main function equivalent if any, though Glossa doesn't strictly have one.
        // We will just export all functions.
        let mut params_str = String::new();
        for (i, param_type) in func.param_types.iter().enumerate() {
            if i > 0 {
                params_str.push_str(", ");
            }
            let ts_type = glossa_type_to_ts(param_type);
            let _ = write!(params_str, "arg{}: {}", i, ts_type);
        }

        let ret_type = func
            .return_type
            .as_ref()
            .map(glossa_type_to_ts)
            .unwrap_or_else(|| "void".to_string());

        let _ = writeln!(
            output,
            "export declare function {}({}): {};\n",
            func.name, params_str, ret_type
        );
    }

    println!();
    println!("   {}", "Γ Λ Ω Σ Σ Α   P T O L E M Y".bold().cyan());
    println!("   {}", "TypeScript Definitions".italic().dim());
    println!();

    let mut table = Table::new();
    table.load_preset(presets::UTF8_FULL);

    table.set_header(vec![
        Cell::new("TypeScript (.d.ts)")
            .add_attribute(Attribute::Bold)
            .fg(Color::Cyan),
    ]);

    let formatted_code = format!("```typescript\n{}\n```", output.trim());
    table.add_row(vec![Cell::new(formatted_code)]);

    println!("{table}");
    println!();

    let output_path = input.with_extension("d.ts");
    if let Err(e) = std::fs::write(&output_path, &output) {
        return Err(miette::miette!("Failed to write definition file: {}", e));
    }

    println!(
        "   {} {}",
        "Saved to:".bold(),
        output_path.display().to_string().cyan()
    );
    println!();

    Ok(())
}

fn glossa_type_to_ts(g_type: &GlossaType) -> String {
    match g_type {
        GlossaType::Number => "number".to_string(),
        GlossaType::String => "string".to_string(),
        GlossaType::Boolean => "boolean".to_string(),
        GlossaType::List(inner) => format!("Array<{}>", glossa_type_to_ts(inner)),
        GlossaType::Set(inner) => format!("Set<{}>", glossa_type_to_ts(inner)),
        GlossaType::Map(k, v) => {
            format!("Record<{}, {}>", glossa_type_to_ts(k), glossa_type_to_ts(v))
        }
        GlossaType::Option(inner) => format!("{} | null", glossa_type_to_ts(inner)),
        GlossaType::Result(ok, err) => {
            format!("{} | {}", glossa_type_to_ts(ok), glossa_type_to_ts(err))
        }
        GlossaType::Struct { name, .. } => name.to_string(),
        _ => "any".to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;
    use tempfile::tempdir;

    #[test]
    fn test_run_ptolemy_success() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api.γλ");
        fs::write(&input_path, "εἶδος Χρήστης ὁρίζειν { ὄνομα ὀνόματος. }.").unwrap();

        let result = run_ptolemy(&input_path);
        assert!(result.is_ok());

        let output_path = input_path.with_extension("d.ts");
        assert!(output_path.exists());
    }

    #[test]
    fn test_run_ptolemy_file_error() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api.γλ");
        fs::write(&input_path, "εἶδος Χρήστης ὁρίζειν { }.").unwrap();

        let output_path = input_path.with_extension("d.ts");
        fs::create_dir(&output_path).unwrap();

        let result = run_ptolemy(&input_path);
        assert!(result.is_err());
        let err_msg = result.unwrap_err().to_string();
        assert!(
            err_msg.contains("Failed to write")
                || err_msg.contains("directory")
                || err_msg.contains("denied")
                || err_msg.contains("Permission")
        );
    }

    #[test]
    fn test_run_ptolemy_with_functions() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api.γλ");

        let source = "
        προσθεσις ὁρίζειν τῷ ξ ἀριθμοῦ τῷ ψ ἀριθμοῦ· δός ξ ψ ἄθροισμα.
        ";
        fs::write(&input_path, source).unwrap();

        let result = run_ptolemy(&input_path);
        assert!(result.is_ok());

        let output_path = input_path.with_extension("d.ts");
        assert!(output_path.exists());

        let ts = fs::read_to_string(&output_path).unwrap();
        assert!(
            ts.contains("export declare function προσθεσις(arg0: number, arg1: number): number;")
        );
    }

    #[test]
    fn test_run_ptolemy_with_function_no_return() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api2.γλ");

        let source = "
        ἐκτύπωσις ὁρίζειν τῷ ξ ἀριθμοῦ· ξ λέγε.
        ";
        fs::write(&input_path, source).unwrap();

        let result = run_ptolemy(&input_path);
        assert!(result.is_ok());

        let output_path = input_path.with_extension("d.ts");
        let ts = fs::read_to_string(&output_path).unwrap();
        assert!(
            ts.contains("export declare function εκτυπωσις(arg0: number): void;")
        );
    }

    #[test]
    fn test_run_ptolemy_nonexistent() {
        let result = run_ptolemy(Path::new("nonexistent_file.γλ"));
        assert!(result.is_err());
    }

    #[test]
    fn test_run_ptolemy_load_error() {
        let dir = tempdir().unwrap();
        // A directory exists but fails to load as source
        let result = run_ptolemy(dir.path());
        assert!(result.is_err());
    }

    #[test]
    fn test_run_ptolemy_analysis_error() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("api.γλ");
        // Use valid tokens but semantically invalid syntax that fails the analyzer
        fs::write(&input_path, "ψ 10 γίγνεται.").unwrap();

        let result = run_ptolemy(&input_path);
        assert!(result.is_err());
    }

    #[test]
    fn test_glossa_type_to_ts() {
        assert_eq!(glossa_type_to_ts(&GlossaType::Number), "number");
        assert_eq!(glossa_type_to_ts(&GlossaType::String), "string");
        assert_eq!(glossa_type_to_ts(&GlossaType::Boolean), "boolean");
        assert_eq!(
            glossa_type_to_ts(&GlossaType::List(Box::new(GlossaType::Number))),
            "Array<number>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Set(Box::new(GlossaType::Number))),
            "Set<number>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Map(
                Box::new(GlossaType::String),
                Box::new(GlossaType::Number)
            )),
            "Record<string, number>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Option(Box::new(GlossaType::Number))),
            "number | null"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Result(
                Box::new(GlossaType::Number),
                Box::new(GlossaType::String)
            )),
            "number | string"
        );
        assert_eq!(glossa_type_to_ts(&GlossaType::Unknown), "any");
    }
}
