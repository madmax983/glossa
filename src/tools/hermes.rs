//! The Hermes (ὁ Ἑρμῆς) - TypeScript Interface Generator
//!
//! This module implements the "Hermes" tool, which inspects the type definitions
//! (`εἶδος`) within a ΓΛΩΣΣΑ program and translates them into TypeScript `interface`
//! declarations.
//!
//! # Purpose
//!
//! Hermes was the messenger of the gods, bridging different worlds. In modern
//! systems, we need to bridge backend code with frontend code. This tool automatically
//! generates TypeScript interfaces directly from ΓΛΩΣΣΑ structures, making frontend
//! integration seamless.
//!
//! # How it Works
//!
//! The [`run_hermes`](crate::tools::hermes::run_hermes) function orchestrates the process:
//! 1. Parses and semantically analyzes the source code.
//! 2. Scans the Abstract Syntax Tree for `TypeDefinition` nodes.
//! 3. Maps ΓΛΩΣΣΑ types (`ἀριθμοῦ`, `ὀνόματος`, etc.) to TypeScript types (`number`, `string`, etc.).
//! 4. Outputs formatted TypeScript code to the terminal using a stylish table.

use crate::semantic::{AnalyzedStatement, GlossaType};
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use comfy_table::{Attribute, Cell, Color, Table, presets};
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::io::{self, IsTerminal};
use std::path::Path;

/// Runs the Hermes tool to generate TypeScript interfaces from Glossa types.
///
/// The Hermes (Ἑρμῆς) tool reads the provided source file, compiles it, and automatically
/// generates corresponding TypeScript `interface` statements for any type definitions found.
///
/// # Examples
///
/// ```rust,no_run
/// use glossa::tools::hermes::run_hermes;
/// use std::path::Path;
///
/// let input = Path::new("schema.γλ");
/// if let Err(e) = run_hermes(&input) {
///     eprintln!("Interface generation failed: {}", e);
/// }
/// ```
///
/// # Errors
///
/// Returns a [`miette::Result`] if the file cannot be read, or if there is a parsing
/// or semantic analysis error during compilation.
pub fn run_hermes(input: &Path) -> Result<()> {
    if !input.exists() {
        return Err(miette::miette!("Ἀρχεῖον οὐχ εὑρέθη: {}", input.display()));
    }

    let is_tty = io::stdout().is_terminal();

    let status = if is_tty {
        Some(Status::start_with_symbol(
            "Ἑρμῆς (Generating TypeScript)",
            "🪽",
        ))
    } else {
        None
    };

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            if let Some(s) = status {
                s.error("Σφάλμα ἀρχείου (File Error)");
            }
            return Err(e);
        }
    };

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            if let Some(s) = status {
                s.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            }
            return Err(e);
        }
    };

    if let Some(s) = status {
        s.success();
    }

    let mut output = String::new();
    output.push_str("// Auto-generated by ΓΛΩΣΣΑ Hermes\n\n");

    let mut generated_count = 0;

    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            generated_count += 1;
            let _ = writeln!(output, "export interface {} {{", name);
            for (field_name, field_type) in fields {
                let ts_type = glossa_type_to_ts(field_type);
                // In TypeScript, Option types usually mean the field is optional
                let is_optional = matches!(field_type, GlossaType::Option(_));
                let opt_marker = if is_optional { "?" } else { "" };
                let _ = writeln!(output, "    {}{}: {};", field_name, opt_marker, ts_type);
            }
            output.push_str("}\n\n");
        }
    }

    if generated_count == 0 {
        output.push_str("// No structs (εἶδος) found in the provided file.\n");
    }

    if is_tty {
        println!();
        println!("   {}", "Γ Λ Ω Σ Σ Α   H E R M E S".bold().cyan());
        println!("   {}", "TypeScript Interfaces".italic().dim());
        println!();

        let mut table = Table::new();
        table.load_preset(presets::UTF8_FULL);

        table.set_header(vec![
            Cell::new("TypeScript Definitions")
                .add_attribute(Attribute::Bold)
                .fg(Color::Cyan),
        ]);

        let formatted_code = format!("```typescript\n{}\n```", output.trim());
        table.add_row(vec![Cell::new(formatted_code)]);

        println!("{table}");
        println!();
    } else {
        println!("{}", output.trim());
    }

    Ok(())
}

fn glossa_type_to_ts(g_type: &GlossaType) -> String {
    match g_type {
        GlossaType::Number => "number".to_string(),
        GlossaType::String => "string".to_string(),
        GlossaType::Boolean => "boolean".to_string(),
        GlossaType::List(inner) => format!("Array<{}>", glossa_type_to_ts(inner)),
        GlossaType::Set(inner) => format!("Set<{}>", glossa_type_to_ts(inner)),
        GlossaType::Map(k, v) => format!("Map<{}, {}>", glossa_type_to_ts(k), glossa_type_to_ts(v)),
        GlossaType::Option(inner) => glossa_type_to_ts(inner), // Handled by optional field marker
        GlossaType::Result(ok, err) => format!(
            "Result<{}, {}>",
            glossa_type_to_ts(ok),
            glossa_type_to_ts(err)
        ), // Assuming a Result type exists in TS environment or we generate one
        GlossaType::Struct { name, .. } => name.to_string(),
        GlossaType::Function { params, returns } => {
            let mut ts_params = String::new();
            for (i, p) in params.iter().enumerate() {
                if i > 0 {
                    ts_params.push_str(", ");
                }
                ts_params.push_str(&format!("arg{}: {}", i, glossa_type_to_ts(p)));
            }
            format!("({}) => {}", ts_params, glossa_type_to_ts(returns))
        }
        GlossaType::Unit => "void".to_string(),
        GlossaType::Unknown => "any".to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    use std::io::Write;
    use tempfile::tempdir;

    #[test]
    fn test_run_hermes_success() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("schema.γλ");
        {
            let mut f = std::fs::File::create(&input_path).unwrap();
            f.write_all("εἶδος Χρήστης ὁρίζειν { ὄνομα ὀνόματος. }.\n".as_bytes())
                .unwrap();
        }
        let result = run_hermes(&input_path);
        assert!(result.is_ok());
    }

    #[test]
    fn test_run_hermes_empty_structs() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("empty.γλ");
        {
            let mut f = std::fs::File::create(&input_path).unwrap();
            f.write_all("«χαῖρε» λέγε.\n".as_bytes()).unwrap();
        }
        let result = run_hermes(&input_path);
        assert!(result.is_ok());
    }

    #[test]
    fn test_run_hermes_file_not_found() {
        let result = run_hermes(std::path::Path::new("non_existent_file.γλ"));
        assert!(result.is_err());
        assert!(result.unwrap_err().to_string().contains("οὐχ εὑρέθη"));
    }

    #[test]
    fn test_run_hermes_parse_error() {
        let dir = tempdir().unwrap();
        let input_path = dir.path().join("error.γλ");
        {
            let mut f = std::fs::File::create(&input_path).unwrap();
            f.write_all("invalid syntax\n".as_bytes()).unwrap();
        }
        let result = run_hermes(&input_path);
        assert!(result.is_err());
    }

    #[test]
    fn test_glossa_type_to_ts() {
        assert_eq!(glossa_type_to_ts(&GlossaType::Number), "number");
        assert_eq!(glossa_type_to_ts(&GlossaType::String), "string");
        assert_eq!(glossa_type_to_ts(&GlossaType::Boolean), "boolean");
        assert_eq!(
            glossa_type_to_ts(&GlossaType::List(Box::new(GlossaType::Number))),
            "Array<number>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Set(Box::new(GlossaType::String))),
            "Set<string>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Map(
                Box::new(GlossaType::String),
                Box::new(GlossaType::Number)
            )),
            "Map<string, number>"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Option(Box::new(GlossaType::Number))),
            "number" // the optionality is handled at the field level, not the type level
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Struct {
                name: "User".into(),
                gender: crate::morphology::Gender::Masculine,
                fields: vec![]
            }),
            "User"
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Function {
                params: vec![GlossaType::Number, GlossaType::String],
                returns: Box::new(GlossaType::Boolean)
            }),
            "(arg0: number, arg1: string) => boolean"
        );
        assert_eq!(glossa_type_to_ts(&GlossaType::Unit), "void");
        assert_eq!(glossa_type_to_ts(&GlossaType::Unknown), "any");
    }
}
