//! The Hermes (ὁ Ἑρμῆς) - TypeScript Exporter
//!
//! This module implements the "Hermes" tool, which inspects the type definitions
//! (`εἶδος`) within a ΓΛΩΣΣΑ program and translates them into TypeScript `interface`
//! definitions.
//!
//! # Purpose
//!
//! Hermes was the messenger of the gods, acting as a translator and bridging
//! different worlds. Similarly, this tool bridges the ancient world of ΓΛΩΣΣΑ
//! with the modern world of TypeScript by automatically generating type definitions.
//!
//! # How it Works
//!
//! The [`run_hermes`](crate::tools::hermes::run_hermes) function orchestrates the process:
//! 1. Parses and semantically analyzes the source code.
//! 2. Scans the Abstract Syntax Tree for `TypeDefinition` nodes.
//! 3. Maps ΓΛΩΣΣΑ types (`ἀριθμοῦ`, `ὀνόματος`, etc.) to TypeScript types (`number`, `string`, etc.).
//! 4. Outputs formatted TypeScript code to the terminal using a stylish, colorful table.

use crate::semantic::{AnalyzedStatement, GlossaType};
use crate::tools::runner::load_source;
use crate::tools::ui::Status;
use comfy_table::{Attribute, Cell, Color, Table, presets};
use crossterm::style::Stylize;
use miette::Result;
use std::fmt::Write;
use std::path::Path;

/// Runs the Hermes tool to generate TypeScript schemas from Glossa types.
///
/// The Hermes (Ἑρμῆς) tool reads the provided source file, compiles it, and automatically
/// generates corresponding TypeScript `interface` statements for any type definitions found.
///
/// # Examples
///
/// ```rust,no_run
/// use glossa::tools::hermes::run_hermes;
/// use std::path::Path;
///
/// let input = Path::new("schema.γλ");
/// if let Err(e) = run_hermes(&input) {
///     eprintln!("Schema generation failed: {}", e);
/// }
/// ```
///
/// # Errors
///
/// Returns a [`miette::Result`] if the file cannot be read, or if there is a parsing
/// or semantic analysis error during compilation.
pub fn run_hermes(input: &Path) -> Result<()> {
    if !input.exists() {
        return Err(miette::miette!("Ἀρχεῖον οὐχ εὑρέθη: {}", input.display()));
    }

    let status = Status::start_with_symbol("Ἑρμῆς (Generating TypeScript Schema)", "📜");

    let source = match load_source(input) {
        Ok(s) => s,
        Err(e) => {
            status.error("Σφάλμα ἀρχείου (File Error)");
            return Err(e);
        }
    };

    let program = match crate::tools::runner::analyze_source(&source) {
        Ok(p) => p,
        Err(e) => {
            status.error("Σφάλμα ἀναλύσεως (Analysis Error)");
            return Err(e);
        }
    };

    status.success();

    let mut output = String::new();
    output.push_str("// Auto-generated by ΓΛΩΣΣΑ Hermes\n\n");

    for stmt in &program.statements {
        if let AnalyzedStatement::TypeDefinition { name, fields } = stmt {
            let _ = writeln!(output, "export interface {} {{", name);
            for (field_name, field_type) in fields.iter() {
                let (ts_type, is_optional) = glossa_type_to_ts(field_type);
                let optional_marker = if is_optional { "?" } else { "" };
                let _ = writeln!(
                    output,
                    "    {}{}: {};",
                    field_name, optional_marker, ts_type
                );
            }
            output.push_str("}\n\n");
        }
    }

    println!();
    println!("   {}", "Γ Λ Ω Σ Σ Α   H E R M E S".bold().yellow());
    println!("   {}", "TypeScript Exporter".italic().dim());
    println!();

    let mut table = Table::new();
    table.load_preset(presets::UTF8_FULL);

    table.set_header(vec![
        Cell::new("TypeScript Schema")
            .add_attribute(Attribute::Bold)
            .fg(Color::Yellow),
    ]);

    let formatted_code = format!("```typescript\n{}\n```", output.trim());
    table.add_row(vec![Cell::new(formatted_code)]);

    println!("{table}");
    println!();

    Ok(())
}

fn glossa_type_to_ts(g_type: &GlossaType) -> (String, bool) {
    match g_type {
        GlossaType::Number => ("number".to_string(), false),
        GlossaType::String => ("string".to_string(), false),
        GlossaType::Boolean => ("boolean".to_string(), false),
        GlossaType::List(inner) => {
            let (inner_ts, _) = glossa_type_to_ts(inner);
            (format!("Array<{}>", inner_ts), false)
        }
        GlossaType::Set(inner) => {
            let (inner_ts, _) = glossa_type_to_ts(inner);
            (format!("Set<{}>", inner_ts), false)
        }
        GlossaType::Map(k, v) => {
            let (k_ts, _) = glossa_type_to_ts(k);
            let (v_ts, _) = glossa_type_to_ts(v);
            (format!("Record<{}, {}>", k_ts, v_ts), false)
        }
        GlossaType::Option(inner) => {
            let (inner_ts, _) = glossa_type_to_ts(inner);
            // We append "| undefined" for cases where the Option is nested in generic structures like Array/List.
            // The `true` flag tells the struct field to append a `?` making the property optional.
            (format!("{} | undefined", inner_ts), true)
        }
        GlossaType::Result(ok, _err) => {
            let (ok_ts, _) = glossa_type_to_ts(ok);
            // In TypeScript, errors are usually thrown or handled differently.
            // For a simple schema representation, we'll extract the 'Ok' value type,
            // but mark it as optional if it can fail.
            (ok_ts, true)
        }
        GlossaType::Struct { name, .. } => (name.to_string(), false),
        GlossaType::Function { params, returns } => {
            let mut param_str = String::new();
            for (i, param) in params.iter().enumerate() {
                let (param_ts, _) = glossa_type_to_ts(param);
                if i > 0 {
                    param_str.push_str(", ");
                }
                param_str.push_str(&format!("arg{}: {}", i, param_ts));
            }
            let (ret_ts, _) = glossa_type_to_ts(returns);
            (format!("({}) => {}", param_str, ret_ts), false)
        }
        GlossaType::Unit => ("void".to_string(), false),
        GlossaType::Unknown => ("any".to_string(), false),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_glossa_type_to_ts() {
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Number),
            ("number".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::String),
            ("string".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Boolean),
            ("boolean".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::List(Box::new(GlossaType::Number))),
            ("Array<number>".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Set(Box::new(GlossaType::String))),
            ("Set<string>".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Map(
                Box::new(GlossaType::String),
                Box::new(GlossaType::Number)
            )),
            ("Record<string, number>".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Option(Box::new(GlossaType::Number))),
            ("number | undefined".to_string(), true)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Result(
                Box::new(GlossaType::Number),
                Box::new(GlossaType::String)
            )),
            ("number".to_string(), true)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Unknown),
            ("any".to_string(), false)
        );
        assert_eq!(
            glossa_type_to_ts(&GlossaType::Unit),
            ("void".to_string(), false)
        );
    }
}
